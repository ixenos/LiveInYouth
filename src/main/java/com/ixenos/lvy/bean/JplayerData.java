package com.ixenos.lvy.bean;

/**
 * Jplayer数据信息
 * @author ixenos
 *
 */
public class JplayerData {
	/*
	 * 歌曲名
	 */
	private String title;
	/*
	 * 歌曲源
	 */
	private String mp3;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getMp3() {
		return mp3;
	}
	public void setMp3(String mp3) {
		this.mp3 = mp3;
	}
}
